public class vehiculos {
    private  String brand;
    private String model;
    private String year;
    private String state;

    public vehiculos(String brand, String model, String year, String state) {
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.state = state;
    }

    public String getBrand() {
        return brand;
    }

    public String getModel() {
        return model;
    }

    public String getYear() {
        return year;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    

    
    




}


